print ('Cálculo da área de um triângulo ')
base = int(input ("Informe a medida da base:"))
altura = int(input ("Informe a medida da altura:"))
área = base*altura/2
print  ('A área do triângulo é:', área)

print ('Cálculo da área de um quadrado ')
lado = int(input ('Informe a medida da base:'))
área = lado**2
print ("A área do quadrado é:", área)

